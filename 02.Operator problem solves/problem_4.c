#include <stdio.h>

int main() {
    int X = 5;

    printf("Increment and Decrement: %d\n", X++);
    X = 5;
    printf("Increment and Decrement: %d\n", ++X);
    X = 5;
    printf("Increment and Decrement: %d\n", X--);
    X = 5;
    printf("Increment and Decrement: %d\n", --X);

    return 0;
}

