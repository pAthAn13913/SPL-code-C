#include <stdio.h>

int main() {
    double doubleVariable = 3.14159; // A double variable
    int booleanVariable = 1; // 0 represents false, 1 represents true

    // Printing the values
    printf("Double Value: %lf\n", doubleVariable);
    printf("Boolean Value: %s\n", booleanVariable ? "true" : "false");

    return 0;
}

