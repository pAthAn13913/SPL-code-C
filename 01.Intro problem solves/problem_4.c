#include <stdio.h>

int main() {
    // Declare and initialize variables
    int integerVariable;
    float floatVariable;
    char charVariable;

    integerVariable = 42;
    floatVariable = 3.14;
    charVariable = 'A';

    // Print the values of the variables
    printf("Integer Value: %d\n", integerVariable);
    printf("Float Value: %f\n", floatVariable);
    printf("Character Value: %c\n", charVariable);

    return 0;
}

